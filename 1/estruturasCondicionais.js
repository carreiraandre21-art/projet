// Condicional ( if )

const idade = 10;



if ( idade < 18 ) {
    console.log( 'menor de idade' );
}
// se a condição for verdadeira, o código dentro do bloco if será executado

if ( idade >= 18 ) {
    console.log( 'maior de idade' );
} else {
    console.log( 'menor de idade' );
}
// se a condição for falsa, o código dentro do bloco else será executado


  