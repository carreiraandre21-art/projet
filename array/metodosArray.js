/*

sort() - ordena os elementos de um array.

reverse() - inverte a ordem dos elementos em um array.

indexOf(element) - retorna o índice da primeira ocorrência de um elemento no array, ou -1 se não for encontrado.

lastIndexOf(element) - retorna o índice da última ocorrência de um elemento no array, ou -1 se não for encontrado.

includes(element) - retorna true se o array contém o elemento especificado, caso contrário retorna false.

join(separator) - junta todos os elementos de um array em uma string, usando o separador especificado.

slice(start, end) - retorna uma cópia de uma parte do array, do índice start até o índice end (não incluindo end).



push () - adiciona um ou mais elementos ao final de um array e retorna o novo comprimento do array.

pop() - remove o último elemento de um array e retorna esse elemento.

shift() - remove o primeiro elemento de um array e retorna esse elemento.

unshift(element1, element2, ...) - adiciona um ou mais elementos ao início de um array e retorna o novo comprimento do array.

splice(start, deleteCount, item1, item2, ...) - altera o conteúdo de um array, removendo elementos existentes e/ou adicionando novos elementos.

filter(callback) - cria um novo array com todos os elementos que passam no teste implementado pela função callback.

map(callback) - cria um novo array com os resultados da chamada de uma função para cada elemento do array.

reduce(callback, initialValue) - aplica uma função a um acumulador e a cada elemento do array (da esquerda para a direita) para reduzi-lo a um único valor.


*/
